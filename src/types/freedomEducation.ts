/**
 * A single winning metric with its real-world example
 * Generated by Opus Judge during report generation
 */
export interface FreedomExample {
  metricId: string;
  metricName: string;
  metricIcon: string;
  winnerScore: number;
  loserScore: number;
  realWorldExample: string;  // AI-generated practical example
}

/**
 * Freedom data for a single category tab
 * Contains all winning metrics and the hero statement
 */
export interface CategoryFreedomData {
  categoryId: CategoryId;
  categoryName: string;
  categoryIcon: string;
  winningMetrics: FreedomExample[];
  heroStatement: string;  // AI-generated joyful summary
}

